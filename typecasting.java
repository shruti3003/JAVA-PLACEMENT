public class typecasting {
    public static void main(String[] args) {
        double a = 23444;
        float b = (float) a;
        System.out.println(b);

        char ch = 'a';

        int number = ch;
        char ch2 = 'b';
        int number2 = ch2;
        System.out.println(number);
        System.out.println(number2);

    }

}
